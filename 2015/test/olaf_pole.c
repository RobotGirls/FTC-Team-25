void move_to_position(int position)
{
	init_path();

	switch (position) {
	case 1:
        // add_segment(52.5, 0, 50);  //move to position 1
        add_segment(20, -90, 40);
        break;
    case 2:
        servo[rightEye] = RSERVO_PERP;
        add_segment(30, -33, 40);   //move to position 2
        break;
    case 3:
        nxtDisplayTextLine(4, "position 3");
        add_segment(33, 45, 40);  //move to position 3
        break;
    }
    stop_path();
  	dead_reckon();
}

void move_to_pole(int count)
{
    init_path();

    switch (count) {
    case 1:
        add_segment(26, 15, 45);
        add_segment(-7.5, 105, 80);
        break;
    case 2:
        add_segment(22, 0, 45);
        add_segment(-7.5, -135, 50);
        break;
    case 3:
        add_segment(27, 27.5, 45);
        add_segment(-13.5, -202, 50);
        break;
    }
    stop_path();
  	dead_reckon();
}

task main()
{
    int i;

    servo[leftEye] = LSERVO_CENTER;
    servo[rightEye] = RSERVO_CENTER;

    init_path();
    add_segment(24, 0, 50);
    stop_path();
    dead_reckon();

    if (SensorValue[carrot] < 60) {
        beep = 3;
        move_to_pole(3);
    }
    else if (SensorValue[carrot] > 200) {
        beep = 2;
        move_to_pole(2);
    }
    else if (SensorValue[carrot] < 80) {
        beep = 1;
        move_to_pole(1);
    }

    for (i = 0; i < beep; i++) {
        playImmediateTone(251, 50);
        wait1Msec(1000);
    }
}
