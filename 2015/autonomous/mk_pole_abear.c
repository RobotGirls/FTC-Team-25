void move_to_pole(int count)
{
    init_path();

    switch (count) {
    case 1:
        add_segment(22, 180, 50);
        add_segment(20, 90, 100);
        break;
    case 2:
        add_segment(-10, 0, 50);
        add_segment(0, -150, 50);
        add_segment(-50, 0, 100);
        break;
    case 3:
        add_segment(15, -90, 50);
        add_segment(45, -90, 50);
        break;
    }
    stop_path();
  	dead_reckon();
}

task raise_hand()
{
    wait1Msec(5000);
    servo[autoElbow] = SERVO_AUTOELBOW_UP;
}

task raise_elbow()
{
    wait1Msec(2000);

    nMotorEncoder[elbow] = 0;

    motor[elbow] = 10;
    while (nMotorEncoder[elbow] <= 4000) {
        nxtDisplayCenteredBigTextLine(3, "%d", nMotorEncoder[elbow]);
    }
    motor[elbow] = 0;
}

task lower_ramp()
{
    nMotorEncoder[motorA] = 0;
    motor[motorA] = RAMP_SPEED_DOWN;
    motor[motorB] = RAMP_SPEED_DOWN;
    while (nMotorEncoder[motorA] <= 360 * 2) {
    }
    motor[motorA] = 0;
    motor[motorB] = 0;
}

/*
 * Should only be called after a subsequent lower
 */
task raise_ramp()
{
    motor[motorA] = RAMP_SPEED_UP;
    motor[motorB] = RAMP_SPEED_UP;
    while (nMotorEncoder[motorA] >= 0) {
    }
    motor[motorA] = 0;
    motor[motorB] = 0;
}


void initialize_robot()
{
    servo[autoElbow] = SERVO_AUTOELBOW_DOWN;
}

task main()
{
    int i;
    int center_position;

    initialize_robot();

    waitForStart();

    startTask(raise_elbow);
    startTask(raise_hand);
    startTask(lower_ramp);

    // initialize_receiver(irr_left, irr_right);

    servo[leftEye] = LSERVO_CENTER;
    servo[rightEye] = RSERVO_CENTER;

    center_position = ultrasound(carrot, -24, US_DIST_POS_1, US_DIST_POS_3);

    startTask(raise_ramp);

    for (i = 0; i < center_position; i++) {
        playImmediateTone(251, 50);
        wait1Msec(1000);
    }

    move_to_pole(center_position);

    while(true) {
        nxtDisplayTextLine(1, "Sensor value: %d", SensorValue[carrot]);
    }
}
