void move_to_pole(int count)                 // Function that moves the robot to the pole
{                                            // based on the position of the center goal.
    init_path();

    switch (count) {
    case 1:
        add_segment(-22, 0, 50);
        add_segment(-20, 180, 100);
        break;
    case 2:
        add_segment(-10.8125, 0, 50);
        add_segment(0, 30, 50);
        add_segment(-50, 0, 100);
        break;
    case 3:
        add_segment(-10, 90, 50);
        add_segment(-45, -90, 50);
        break;
    }
    stop_path();
  	dead_reckon();
}

void score_center_goal(int arm_dist, int dump_dist)     // Function that moves the robot to the correct distance, raises
{                                                       // the arm, and spins the balls into the tube.
    move_to_object(carrot, -5, arm_dist);                // Move robot to distance where it can raise arm.

    motor[shoulder] = 15;
    while (nMotorEncoder[shoulder] < UPCOUNTS) {        // While encoder counts is less than UPCOUNTS, move shoulder at 15
    }                                                   // power. When encoder counts surpasses UPCOUNTs, stop shoulder.
    motor[shoulder] = 0;

    servo[arm] = SERVO_ARM_EXTENDED;                    // Move the arm to the extended position.

    wait1Msec(1500);

    move_to_object(carrot, -5, dump_dist);              // Move robot to position where it can dump balls in tube.

    servo[door] = SERVO_DOOR_CENTERGOAL_RAMP;           // Open the servo door (releases balls).

    servo[brush] = 255;                                 // Rotate brush (spins balls into center goal).
    wait1Msec(5000);
    servo[brush] = 0;
}

task main()
{
    int i;
    int center_position;

    initialize_receiver(irr_left, irr_right);

    servo[leftEye] = LSERVO_CENTER;
    servo[rightEye] = RSERVO_CENTER;

    waitForStart();

    center_position = ultrasound(carrot, -24, US_DIST_POS_1, US_DIST_POS_3);    // Sets the value integer center_position to 1, 2, or 3
                                                                                // based on ultrasound sensor readings.
    for (i = 0; i < center_position; i++) {
        playImmediateTone(251, 50);                                             // Beep the center structure position for 0.25 second.
        wait1Msec(250);
    }

    if (center_position == 3) {
        move_to_beacon_mux(irr_left, irr_right, 20, false);
        score_center_goal(35, 31);
    } else {
        move_to_pole(center_position);
    }

    while(true) {
        nxtDisplayTextLine(1, "Sensor value: %d", SensorValue[carrot]);
    }
}
